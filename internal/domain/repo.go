package domain

///////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Interface
//
// Repo (list.Item):
//	- Represents a repository

type Repo struct {
	Name, Path string
}

func (r Repo) Title() string       { return r.Name }
func (r Repo) Description() string { return r.Path }
func (r Repo) FilterValue() string { return r.Name }

// End "Interface"
